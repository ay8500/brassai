<p>- 1797 illetve 1800 június 15-én született Torockószentgyörgyön (saját bevallása szerint 1797-ben - hivatalos iratba nincs bejegyezve, mivel nagyapja keresztelte Torockószent-györgyön). Édesapja Brassai Sámuel, Torockószentgyörgyön iskolamester, unitárius pap. Édesanyja, Koncz Krisztina, unitárius papcsaládból származó tanult, igen művelt nő. A család 1827-ig lakik Torockószentgyörgyön, majd idősebb Brassai Sámuel Szentmihályfalvára költözik, itt lesz unitárius pap haláláig, 1837-ig.</p>
<p>- 1813: kisebb megszakításokkal 1820-ig a Kolozsvári Unitárius Kollégiumban tanul</p>
<p>- 1821: Zsibón a Wesselényi Miklós meghívására rendezi ennek könyvtárát </p>
<p>- 1821-1837: a Bethlen és Kendeffy családoknál magántanár, zenét, nyelveket tanít. Rettegen, Iklódon és Bécsben él ezekben az években.</p> 
<p>- 1831: megjelennek első cikkei</p>
<p>- 1832: megjelenik első jelentős esztétikai tanulmánya: "A gyönyörűségről"</p>
<p>- 1834: szerkeszti a Vasárnapi Újságot, mely négy oldalban, magyar nyelven jelenik meg; "közhasznú ismeretekkel" neveli, tanítja Erdély olvasótáborát. Kolozsvár kulturális-társadal-mi életének minden jelentős szervezkedésében és megnyilatkozásában részt vesz.</p>
<p>- az 1836-ban létesült Úrikaszinó alapító tagja (ez egy értelmiséget összefogó, klub jellegű társaság, mely Széchenyi István kezdeményezésére alakult; nagy jelentősége van Kolozsvár kulturális-gazdasági-társadalmi életében)</p>
<p>- 1837: a Kolozsvári Unitárius Kollégium tanára. Magyarul tanít, annak ellenére, hogy a zsinat csak 1841-ben engedélyezi ezt (addig a tanítás hivatalos nyelve a latin)</p>
<p>- 1834: földrajzkönyve jelenik meg magyar nyelven "Bevezetés a Világ, Föld és Státusok ismeretébe" címmel</p>
<p>- tanári munkássága alatt tankönyveket ír, mert látja ezek hiányát. Megindítja a "Kék könyvtár" sorozatot (fűzött, olcsó kiadású könyvecskék); német, francia nyelvkönyvet szerkeszt, matematika tankönyvet "Számító Szokrátesz" címmel, algebrai gyakorlókönyvet ad ki, valamint a "Magyar Fűvészkönyvet" és a "Latin nyelvtant".</p> 
<p>- 1865: megjelenik egyik legjelentősebb tanulmánya "A Módszerről" címmel, melyben pedagógiai munkásságának tapasztalatait foglalja össze, tulajdonképpen a magyar didaktika tudomány alapjait fekteti le ezzel a tanulmányával</p>
<p>	Tanítsunk "keveset, jól és lassan" - vallja Brassai - "Tanítsunk igazságot, legyünk fokozatosak és következetesek."</p>
<p>- 1848-as szereplése miatt Pestre menekül, majd a szabadságharc bukása után Szatmár és Máramaros megyékben bújkál</p>
<p>- 1850-59: Kolozsvártól távol, Budapesten él édesanyjával, akit édesapja halála után (1837) magához vesz</p>
<p>- Erkel Ferenccel, Hubay Miklós hegedűművésszel barátkozik, gyakran megfordul Eötvös József házában, ahol Eötvösnével kamarazenélnek - négykezeseket játszanak</p>
<p>- 1859: visszakerül Kolozsvárra és az Unitárius Kollégium tanára 1862-ig, amikor lemond tanári állásáról és a Kollégiumnak 1000 forintos alapítványt tesz</p>
<p>- 1859: az Erdélyi Múzeum alapító tagja, őre, majd igazgatója. A Múzeum Mikó-kerti házában lakik édesanyjával.</p>
<p>- 1862: meghal édesanyja</p>
<p>- 1865: a Magyar Tudományos Akadémia rendes tagjává választja</p>
<p>- 1872: az akkor alakult Kolozsvári Ferencz József Tudományegyetem rendes tanára a matematikai tanszéken, de tanít szanszkrit nyelvet és nyelvbölcseletet is</p>
<p>- 1881: az egyetem természettudományi karán tanuló, szegény sorú hallgatók számára létesít alapítványt</p>
<p>- 1883: egyetemi tanárként nyugdíjazzák</p>
<p>- 1884: a Dávid Ferenc Egylet alapító tagja és elnöke</p>
<p>- 1887: a Kolozsvári Zenetársaság alapító tagja</p>
<p>- 1897 májusától gyengélkedik a Kolozsvári Vöröskeresztes Kórházban, itt látogatja meg Hubay Jenő hegedűművész, aki kérésére a Bach Chacon-ját játssza neki búcsúként</p>
<p>- 1897 június 24-én hunyt el, az unitárius templomban ravatalozzák fel, a Házsongárdi temetőben helyezik örök nyugalomra</p>
